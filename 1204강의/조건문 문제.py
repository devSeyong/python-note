'''
학점 계산 프로그램을 만드세요.

요구사항:
1. 점수와 출석률을 입력받습니다.
2. 다음 기준에 따라 학점을 출력하세요:
   - 출석률이 75% 미만이면 무조건 "F"
   - 95점 이상: "A+"
   - 90-94점: "A"
   - 85-89점: "B+"
   - 80-84점: "B"
   - 75-79점: "C+"
   - 70-74점: "C"
   - 65-69점: "D+"
   - 60-64점: "D"
   - 60점 미만: "F"
3. 최종 출력: "당신의 학점은 ○○입니다."
'''


점수 = int(input("점수를 입력해주세요(0-100): "))
출석률 = int(input("출석률을 입력해주세요(0-100): "))

def 학점계산기(점수,출석률):
    if 출석률 < 75:
        return 'F'
    if 점수 >= 95:
        학점 = 'A'
    elif 점수 >= 90:
        학점 = 'B'
    elif 점수 >= 85:
        학점 = 'C'
    elif 점수 >= 80:
        학점 = 'D'
    elif 점수 >= 75:
        학점 = 'E'
    else:
        return 'F'
    return 학점
등급 = (학점계산기(점수,출석률))
print(f"당신의 학점은 {등급}입니다.")